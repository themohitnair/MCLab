		AREA RESET, CODE, READONLY
N RN 1
EN RN 2
SUM RN 3
ENTRY	
		MOV N, #5; INITIALIZING NUMBER OF ITERATIONS
		MOV EN, #2; EVEN NUMBER
		MOV R4, #0X40000000; MEMORY LOCATION
LOOP	ADD SUM, SUM, EN; ADDING EVEN NUMBER TO SUM
		ADD EN, EN, #2; NEXT EVEN NUMBER
		SUBS N, N, #1; DECREMENT ITERATOR
		BNE LOOP
		STR SUM, [R4]; STORING SUM AT ADDRESS INSIDE R4
GO 		B GO
		END
		