		AREA RESET, CODE, READONLY
NUM RN 1
D RN 2
Q RN 3
DSR RN 4
DND RN 5
R RN 6
SUM RN 7
ENTRY	
		MOV SUM, #0;
		MOV NUM, #69; NUMBER UNDER CONSIDERATION
		MOV DSR, #10; TO DIVIDE BY 10
ALOOP	MOV DND, NUM; NUMBER MOVES TO DIVIDEND
		BL DIV; BRANCH TO DIVISION SUBROUTINE
		MOV NUM, Q; QUOTIENT IS NOW NUM
		MOV D, R; DIGIT IS REMAINDER
		ADD SUM, SUM, R; ADD REMAINDER TO SUM
		CMP NUM, #0; COMPARE NUMBER AND 0 TO STOP THE LOOP
		BNE ALOOP
GO 		B GO
DIV		MOV Q, #0; QUOTIENT INITIALIZED TO 0
BLOOP	SUBS DND, DND, DSR; SUBTRACT DIVISOR FROM DIVIDEND
		ADDPL Q, Q, #1; ADD QUOTIENT IF RESULT IS POSITIVE
		BPL BLOOP; REPEAT SUBTRACTION IF POSITIVE
		ADDMI R, DND, DSR; IF NEGATIVE, ADD DIVISOR BACK TO DIVIDEND TO GET THE REMAINDER
		BX LR; RETURN TO CALL
		END